public class BitmapFont
{
    public Texture Texture { get; private set; } = null!;
    public int GlyphWidth { get; private set; }
    public int GlyphHeight { get; private set; }
    public int CharacterSpacing { get; set; } = 1;
    public int LineSpacing { get; set; } = 4;

    private readonly Dictionary<char, GlyphInfo> _glyphs = new();

    public struct GlyphInfo
    {
        public int X, Y, Width, Height;
        public float AdvanceX;
    }

    public BitmapFont(Texture texture, int glyphWidth, int glyphHeight, string characters)
    {
        Texture = texture;
        GlyphWidth = glyphWidth;
        GlyphHeight = glyphHeight;

        var cols = texture.Width / glyphWidth;

        for (int i = 0; i < characters.Length; i++)
        {
            var col = i % cols;
            var row = i / cols;
            _glyphs[characters[i]] = new GlyphInfo
            {
                X = col * glyphWidth,
                Y = row * glyphHeight,
                Width = glyphWidth,
                Height = glyphHeight,
                AdvanceX = glyphWidth + CharacterSpacing
            };
        }
    }

    public GlyphInfo? GetGlyph(char c)
    {
        if (_glyphs.TryGetValue(c, out var glyph))
            return glyph;
        if (_glyphs.TryGetValue('?', out var fallback))
            return fallback;
        return null;
    }

    public float MeasureWidth(string text)
    {
        float w = 0;
        foreach (var c in text)
        {
            var g = GetGlyph(c);
            w += g?.AdvanceX ?? GlyphWidth;
        }
        return w;
    }

    public static BitmapFont CreateDefault()
    {
        const int gw = 8;
        const int gh = 8;
        const int cols = 16;
        var chars = " !\"#$%&'()*+,-./0123456789:;<=>?@ABCDEFGHIJKLMNOPQRSTUVWXYZ[\\]^_`abcdefghijklmnopqrstuvwxyz{|}~";

        var rows = (chars.Length + cols - 1) / cols;
        var texW = cols * gw;
        var texH = rows * gh;
        var data = new byte[texW * texH * 4];

        for (int i = 0; i < chars.Length; i++)
        {
            var ch = chars[i];
            var col = i % cols;
            var row = i / cols;

            for (int py = 0; py < gh; py++)
            {
                var rowByte = GetGlyphData(ch, py);
                for (int px = 0; px < gw; px++)
                {
                    var on = (rowByte & (1 << px)) != 0;
                    var dx = col * gw + px;
                    var dy = row * gh + py;
                    var idx = (dy * texW + dx) * 4;
                    byte v = on ? (byte)255 : (byte)0;
                    data[idx + 0] = v;
                    data[idx + 1] = v;
                    data[idx + 2] = v;
                    data[idx + 3] = on ? (byte)255 : (byte)0;
                }
            }
        }

        var tex = new Texture(texW, texH, data);
        return new BitmapFont(tex, gw, gh, chars);
    }

    private static byte GetGlyphData(char c, int row)
    {
        var i = (int)c;
        if (i < 0 || i >= 128) return 0;
        if (i < 32) return 0;
        if (i > 126) return 0;
        return FontData[i - 32, row];
    }

